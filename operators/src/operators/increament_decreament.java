package operators;

public class increament_decreament {
public static void main(String[] args) {
	int y=15;
	System.out.println("post increament "+(y++));
	System.out.println("post decreament "+(y--));
	System.out.println("pre increament "+(++y));
	System.out.println("pre decreament "+(--y));

}
}
